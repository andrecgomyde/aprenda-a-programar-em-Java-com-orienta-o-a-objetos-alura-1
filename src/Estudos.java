import static java.lang.System.*;

public class Estudos {

    public static void main(String[] args) {

        System.out.println("Confira alguns atalhos para utilizar na IDE IntelliJ ao programar em Java:");

        System.out.println("(alt + 1) Minimiza o menu lateral de estruturas de arquivos do projeto;");

        System.out.println("(crtl + space) Autocompletar;");

        System.out.println("(crtl + alt + l) Formatação automática do código;");

        System.out.println("(shift + F10) Executa a classe;");

        System.out.println("(alt + enter) Ao utilizar o atalho com o cursor do mouse encima de alguma parte do código, o InteliJ informa qual é a correção sugerida;");

        System.out.println("(crtl + F8) Adiciona ou remove o breakpoint de uma linha;");

        System.out.println("(shift + F9) Inicia o debug a partir de um breakpoint;");
    }
}
